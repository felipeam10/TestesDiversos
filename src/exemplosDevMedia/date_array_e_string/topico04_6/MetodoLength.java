package exemplosDevMedia.date_array_e_string.topico04_6;

public class MetodoLength {

    public static void main(String[] args) {

        /**
         * O método lengh() retorna um int indicando o tamanho da String, ou seja, a quantidade de caracteres que há na String atual.
         * É comum ser utilizado em loops for e blocos de condição if. O trecho a seguir demonstra a aplicação deste método:
         */

        System.out.println(" Java ".length());
        System.out.println(" Java ".trim().length());
    }
}
